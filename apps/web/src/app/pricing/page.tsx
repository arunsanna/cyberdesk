export default async function Pricing({
  searchParams,
}: {
  searchParams: { [key: string]: string | string[] | undefined }
}) {
  const selectedTierSlug =
    typeof searchParams.tier === 'string' ? searchParams.tier : 'pro'
  const selectedTier =
    tiers.find((tier) => tier.slug === selectedTierSlug) ?? tiers[1]

  // Fetch user and profile data on the server
  const supabase = createClient();
  let user = null;
  let profile: Profile | null = null;
  
  // Check if user is authenticated
  const { data: { user: authUser } } = await supabase.auth.getUser();
  
  if (authUser) {
    user = authUser;
    // Query the profiles table for the user
    const { data: userProfile, error } = await supabase
      .from('profiles')
      .select('*')
      .eq('id', user.id)
      .single();
    
    if (!error) {
      profile = userProfile;
    }
  }

  return (
    <main className="overflow-hidden">
      <GradientBackground />
      <Container>
        <Navbar />
      </Container>
      <Header />
      <ClientPricingCards tiers={tiers} user={user} profile={profile} />
      {/* <PricingTable selectedTier={selectedTier} /> */}
      {/* <Testimonial /> */}
      <div className="h-16"/>
      <Footer />
    </main>
  )
}
